#pragma once

typedef struct vec2
{
    float x;
    float y;
} vec2;

typedef struct vec3
{
    float x;
    float y;
    float z;
} vec3;

typedef struct vec4
{
    float x;
    float y;
    float z;
    float w;
} vec4;

// Vector2 with doubles
typedef struct vec2_d
{
    double x;
    double y;
} vec2_d;

// Vector3 with doubles
typedef struct vec3_d
{
    double x;
    double y;
    double z;
} vec3_d;

// Vector4 with doubles
typedef struct vec4_d
{
    double x;
    double y;
    double z;
    double w;
} vec4_d;

// Vector2 with ints
typedef struct vec2_i
{
    int x;
    int y;
} vec2_i;

// Vector3 with ints
typedef struct vec3_i
{
    int x;
    int y;
    int z;
} vec3_i;

// Vector4 with ints
typedef struct vec4_i
{
    int x;
    int y;
    int z;
    int w;
} vec4_i;